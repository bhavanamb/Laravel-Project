    public function update(Request $request, $id)
    {
        $user = User::find($id);
         $request->validate([
          'email' => 'required',
        ]); 
        $user->name = $request->get('name');
        $user->email = $request->get('email');
        $user->address = $request->get('address');
        $user->save();
        return redirect('customer')->with('success','Update Successful');
    }

